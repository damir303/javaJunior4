package lesson8;

public class Comments {
    private String comm;
    private int liky;

    public Comments() {
    }

    public Comments(String comm, int liky) {
        this.comm = comm;
        this.liky = liky;
    }

    public String getComm() {
        return comm;
    }

    public void setComm(String comm) {
        this.comm = comm;
    }

    public int getLiky() {
        return liky;
    }

    public void setLiky(int liky) {
        this.liky = liky;
    }
}
